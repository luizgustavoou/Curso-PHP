    #Exemplo 2 (Pós Decremento):
    $c = 0;
    echo $c--; //0
    echo $c--; //-1
    echo $c; //-2

    #Exemplo 3 (Pré-Incremento):
    $c = 0;
    echo ++$c; //1 
    echo ++$c; //2

    #Exemplo 4 (Pré-Decremento):
    $c = 0;
    echo --$c; //-1
    echo --$c; //-2




